
public class ForWhile_DoWhile {

	public static void main(String[] args) {
		//For Döngüsü
				for (int i = 1; i < 10; i++) {
					System.out.println(i);
				}
				
				
				//While Döngüsü
				int a = 1;
				while (a<=10) {
					System.out.println(a);
					a++;
				}
				
				//Do_While Döngüsü
				//14. video

	}

}
